<?php
	/* ********************************************************
	 * ********************************************************
	 * ********************************************************/
	class OrderBo {

		protected $dao;
	  
		/* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public function __construct() {
			$this->dao = new OrderDao(new MysqlDatabaseBo());
		}

		/* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public function create(OrderDo $do) {
			return $this->dao->create(
				[
					$do->tavern_items_id,
					$do->user_id,
					$do->amount
				]
			);
		}
	}
?>